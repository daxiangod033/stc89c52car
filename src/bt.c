#include "bt.h"

void UartInit(void)		//9600bps	@11.0592MHz
{
	SCON = 0x50;		//8位数据,可变波特率
	PCON &=~0x80;
	TMOD &= 0x0F;		//清除定时器1模式位
	TMOD |= 0x20;		//设定定时器1为8位自动重装方式
	TL1 = 0xFD;		//设定定时初值
	TH1 = 0xFD;		//设定定时器重装值
	
	//因为串口在通信时需要用到中断机制，所以需要打开中断总开关EA，串口中断开关ES
	ES = 1;		//打开串口的中断设置
	EA = 1;
	
	TR1 = 1;		//打开定时器1
	
	P1 = 0;
}


